package com.mobitribe.open.censor.type

import java.util.regex.Pattern

class EmailCensor : Censor() {

    var emailPatterRegex = "(?:[a-z0-9!#\$%&'*+/=?^_`{|}~-]+(?:\\.[a-z0-9!#\$%&'*+/=?^_`{|}~-]+)*|\"(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21\\x23-\\x5b\\x5d-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])*\")\\s*@\\s*(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\\[(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?|[a-z0-9-]*[a-z0-9]:(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21-\\x5a\\x53-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])+)\\])"
    val emailPattern: Pattern =
        Pattern.compile(emailPatterRegex, Pattern.CASE_INSENSITIVE)

    override fun getCensoredText(text: String): String {
        return emailPattern.matcher(text).replaceAll(placeHolder)
    }

    override fun changePattern(pattern: String): EmailCensor {
        this.emailPatterRegex = pattern
        return this
    }
}